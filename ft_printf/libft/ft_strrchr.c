#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = ft_strlen(s);
	str = (char *)s;
	while (i >= 0)
	{
		if ((unsigned char)c == str[i])
			return (&str[i]);
		i--;
	}
	return (NULL);
}
